## 検索クエリ<hr>
本サーバーでは検索クエリでのソートと絞り込みをサポートしています

### ソート
検索時に `sort:<ソート方法>` または `s:<ソート方法>` で
指定した方法で並べ替えることができます。
#### 対応ソート方法
- createdDate
  - 譜面の登録日でソートします
  - 代わりに `c` と略しても機能します
- updatedDate
  - 譜面の更新日でソートします
  - 代わりに `u` と略しても機能します
- rating
  - 譜面の難易度でソートします
  - 代わりに `r` と略しても機能します
- name
  - 譜面の名前(ID)でソートします
  - 代わりに `n` と略しても機能します

### ジャンル絞り込み
検索時に `genre:<ジャンル名>` で 指定ジャンルのみの絞り込みが可能です

### 難易度絞り込み
検索時に `rating:難易度` で 指定した範囲の難易度で絞り込みが可能です
難易度の指定は `5-` `-10` `20-30` `1204` のように指定してください

### 検索クエリの例
**Daydream genre:anime**
Daydream という単語が含まれる アニメの譜面のみを表示します

**Daydream genre:anime rating:10-20**
Daydream という単語が含まれる 難易度が10-20の譜面のみを表示します

