#### オフセット一覧
- レベル音オフセット
  - 今開いている曲にのみ適用
    - 特定の譜面だけやけにズレている場合使用
    - 通常は0のままでOK
- サーバー音オフセット
  - 同じ配信元サーバーのすべてに適用
  - 通常は0のままでOK
- デバイス音オフセット
  - Sonolus内すべてで利用(基準)
    - 通常これだけを設定すればOK
- エンジン入力オフセット
  - 同じエンジンを使う譜面、全てに適用
  - 通常は0のままでOK
- デバイス入力オフセット
  - タッチセンサーの調子が悪い端末でのみ使用
  - 通常は0のままでOK
<br>
### 譜面プレイ
  1. Sonolusを開き、「開始」 をタップする。
  2. 「ポテト」を選択する
  3. 「レベル」を選択し、好きな譜面を探す
  4.  譜面を選んで、「プレイ」をタップして レッツプレイ!
<br>
### (任意) 譜面設定
レベルの「プレイ」の隣にある「設定」を開くと、細かな設定を変更して遊ぶことができます。ここでは特にその中でも素材を取り上げます。Sonolusの素材は通常すべてのゲームエンジンで互換性があるようになっており、例えば、他サーバーのバンドリのスキンを使ってプロセカエンジンで遊ぶことができます。ここに記載されていない、他サーバーの素材も利用して、好きな組み合わせで遊んでみましょう。

#### 素材一覧
- スキン
  - ノーツとステージのデザインに反映される 見た目
- 背景
  - ステージ後ろに表示される(通常1枚画像の)背景
- SE
  - ノーツやステージをタップしたときに流れる音
- パーティクル
  - ノーツをタップしたときに発生するエフェクトです。エンジン次第であまりキレイに出ないことも
- エンジン(変更不可)
  - 音ゲーとして遊べるようにするための基盤、譜面がエンジンに依存しているため変更できない仕様
