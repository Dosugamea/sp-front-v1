## サーバーアドレス<hr>
本サイトの譜面一覧を取得するためにはサーバーアドレスを登録する必要があります。

### 一般用 サーバーアドレス
本サーバーのメインアドレスは **https://servers.purplepalette.net** です。このアドレスを入力すれば公開状態のすべての譜面を一覧表示できます

### テストプレイ用 サーバーアドレス
譜面を投稿する際、譜面を全体公開前にテストプレイする機能があります。ユーザーページに記載のある**https://servers.purplepalette.net/tests/kafuuchino** という形式のアドレスをSonolusで入力し、選択することでテストプレイできます。
公開前に他の人にテストしてもらいたい場合はこのアドレスを共有することで試してもらうことができます。何らかの理由でアドレスを変えたい場合は、変更することも可能です。
※テストプレイ用アドレスからはテスト状態のすべての譜面がプレイできます、1つのみを共有することはできません。

### ユーザー個別 サーバーアドレス
特定のユーザーの投稿した譜面のみを表示したい場合ユーザー個別アドレスが便利です。
**https://servers.purplepalette.net/users/hoge** のようにサーバーアドレス欄に入れると
指定したユーザーの譜面のみを表示することができます。
※現在ユーザーIDが表示されていないため機能していません、実装をお待ち下さい
